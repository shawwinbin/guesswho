import type { HistoricalFigure } from '../types/figure.js'

type Difficulty = HistoricalFigure['difficulty']
type Seed = readonly [
  id: string,
  name: string,
  aliases: readonly string[],
  era: string,
  role: string,
  gender: string,
  keywords: readonly string[],
  difficulty: Difficulty,
  bornYear?: number,
  diedYear?: number,
]

const artistRoles = new Set(['诗人', '作家', '书法家', '画家', '文学家', '戏曲家'])
const militaryRoles = new Set(['军事家', '武将', '将军', '统帅', '起义领袖'])
const philosopherRoles = new Set(['思想家', '哲学家', '教育家'])
const scientistRoles = new Set(['科学家', '数学家', '医学家', '天文学家', '发明家', '农学家'])
const politicianRoles = new Set(['皇帝', '政治家', '改革家', '官员', '宰相', '谋士', '外交家', '革命家', '起义领袖'])

function buildChineseFigure(seed: Seed): HistoricalFigure {
  const [id, name, aliases, era, role, gender, keywords, difficulty, bornYear, diedYear] = seed

  return {
    id,
    name,
    aliases: [...aliases],
    era,
    role,
    gender,
    region: '中国',
    keywords: [...keywords],
    difficulty,
    isAlive: false,
    isChinese: true,
    isMilitaryLeader: militaryRoles.has(role) || keywords.includes('军事') || keywords.includes('战争'),
    isPhilosopher: philosopherRoles.has(role),
    isArtist: artistRoles.has(role) || keywords.includes('文学') || keywords.includes('艺术'),
    isScientist: scientistRoles.has(role),
    isPolitician: politicianRoles.has(role) || keywords.includes('政治'),
    bornYear,
    diedYear,
  }
}

