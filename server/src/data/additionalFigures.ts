import type { HistoricalFigure } from '../types/figure.js'

type Difficulty = HistoricalFigure['difficulty']
type Seed = readonly [
  id: string,
  name: string,
  aliases: readonly string[],
  era: string,
  role: string,
  gender: string,
  region: string,
  keywords: readonly string[],
  difficulty: Difficulty,
  bornYear?: number,
  diedYear?: number,
]

const artistRoles = new Set(['诗人', '作家', '音乐家', '画家', '剧作家', '雕塑家', '建筑师'])
const militaryRoles = new Set(['军事家', '将军', '征服者', '统帅'])
const philosopherRoles = new Set(['思想家', '哲学家', '宗教领袖'])
const scientistRoles = new Set(['科学家', '数学家', '发明家', '医学家', '天文学家', '物理学家', '化学家', '生物学家'])
const politicianRoles = new Set(['皇帝', '国王', '女王', '政治家', '总统', '首相', '改革家', '革命家', '征服者', '法老'])

function buildFigure(seed: Seed): HistoricalFigure {
  const [id, name, aliases, era, role, gender, region, keywords, difficulty, bornYear, diedYear] = seed

  return {
    id,
    name,
    aliases: [...aliases],
    era,
    role,
    gender,
    region,
    keywords: [...keywords],
    difficulty,
    isAlive: false,
    isChinese: false,
    isMilitaryLeader: militaryRoles.has(role) || keywords.includes('战争') || keywords.includes('军事'),
    isPhilosopher: philosopherRoles.has(role),
    isArtist: artistRoles.has(role) || keywords.includes('文学') || keywords.includes('艺术'),
    isScientist: scientistRoles.has(role),
    isPolitician: politicianRoles.has(role) || keywords.includes('政治'),
    bornYear,
    diedYear,
  }
}

